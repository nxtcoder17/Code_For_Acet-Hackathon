  sno INT PRIMARY KEY AUTO_INCREMENT,
  first VARCHAR(6) default 'FALSE',
  second VARCHAR(6) default 'FALSE',
  third VARCHAR(6) default 'FALSE',
  fourth VARCHAR(6) default 'FALSE',
  fifth VARCHAR(6) default 'FALSE',
  sixth VARCHAR(6) default 'FALSE',
  seventh VARCHAR(6) default 'FALSE'
);
